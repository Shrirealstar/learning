#We have 4 types of number systems

# 1. Binary (will use base as 2) -> 0,1
a = bin(12)
print(a)

b = 0b1010011
print(b)

# 2. decimal (will use base as 10) -> 0 - 10

# 3. Octal (will use base as 8) -> 0 - 8
c = oct(3443)
print(c)

# 4. Hexa decimal (will use base as 16) 0 - 9 and a - f
d = hex(2342323)
print(d)
